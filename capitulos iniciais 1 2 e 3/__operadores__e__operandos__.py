
#atribuição de valores as variáveis
a=11
b=5

print('levando em consideracao que a={0} e b={1},temos...'.format(str(a),str(b)))

#soma
print('soma de {0} com {1} é = {2}'.format(str(a), str(b), str(a + b)))

#subtracao
print('subtracao de {0} com {1} é = {2}'.format(str(a),str(b),str(a-b)))

#divisão
print('divisao de {0} por {1} é = {2}'.format(str(a),str(b),str(a/b)))

#resto da divisao
print('resto da divisao de {0} por {1} e = {2}'.format(str(a),str(b),str(a%b)))

#obter apenas a parte inteira da divisao
print('a parte inteira da divisao de {0} por {1} e = {2}'.format(str(a),str(b),str(a//b)))

#potênciação
print('a potencia de {0} com {1} é = {2}'.format(str(a),str(b),str(a**b)))




